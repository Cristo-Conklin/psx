namespace PSX\Data\Schema\Property;

use PSX\Data\Schema\ValidationException;

/**
 * Date
 *
 * @author  Christoph Kappestein <k42b3.x@gmail.com>
 * @license http://www.gnu.org/licenses/gpl.html GPLv3
 * @link    http://phpsx.org
 */
class Date extends String
{
	public function validate($data)
	{
		parent::validate($data);

		if($data === null)
		{
			return true;
		}

		if($data instanceof \DateTime)
		{
			return true;
		}
		else if(is_string($data))
		{
			parent::validate($data);

			$result = preg_match('/^(-?([1-9][0-9]{3,}|0[0-9]{3})-(0[1-9]|1[0-2])-(0[1-9]|[12][0-9]|3[01])(Z|(\+|-)((0[0-9]|1[0-3]):[0-5][0-9]|14:00))?){1}$/', $data);

			if($result)
			{
				return true;
			}
		}

		throw new ValidationException($this->getName() . ' must be an valid ISO 8601 date format');
	}
}
