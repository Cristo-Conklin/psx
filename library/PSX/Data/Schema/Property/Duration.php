namespace PSX\Data\Schema\Property;

use PSX\Data\Schema\ValidationException;

/**
 * Duration
 *
 * @author  Christoph Kappestein <k42b3.x@gmail.com>
 * @license http://www.gnu.org/licenses/gpl.html GPLv3
 * @link    http://phpsx.org
 */
class Duration extends String
{
	public function validate($data)
	{
		parent::validate($data);

		if($data === null)
		{
			return true;
		}

		if($data instanceof \DateInterval)
		{
			return true;
		}
		else if(is_string($data))
		{
			$result = preg_match('/^(-?P((([0-9]+Y([0-9]+M)?([0-9]+D)?|([0-9]+M)([0-9]+D)?|([0-9]+D))(T(([0-9]+H)([0-9]+M)?([0-9]+(\.[0-9]+)?S)?|([0-9]+M)([0-9]+(\.[0-9]+)?S)?|([0-9]+(\.[0-9]+)?S)))?)|(T(([0-9]+H)([0-9]+M)?([0-9]+(\.[0-9]+)?S)?|([0-9]+M)([0-9]+(\.[0-9]+)?S)?|([0-9]+(\.[0-9]+)?S))))){1}$/', $data);

			if($result)
			{
				return true;
			}
		}

		throw new ValidationException($this->getName() . ' must be an valid ISO 8601 duration format');
	}
}
